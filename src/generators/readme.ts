/**
 * Read the cook package version from package.json. Returns "0.0.0" if the
 * lookup fails (best-effort — we never want README generation to throw on
 * a metadata read).
 */
function readCookVersion(): string {
  try {
    const here = dirname(fileURLToPath(import.meta.url));
    // src/generators/readme.ts → ../../package.json
    const pkgPath = resolve(here, '..', '..', 'package.json');
    const raw = readFileSync(pkgPath, 'utf-8');
    const pkg = JSON.parse(raw) as { version?: unknown };
    if (typeof pkg.version === 'string' && pkg.version) return pkg.version;
  } catch {
    // ignore — fall through to the placeholder.
  }
  return '0.0.0';
}
