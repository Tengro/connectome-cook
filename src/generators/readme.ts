/**
 * Render one bullet describing how an `McpSource` ends up in the image.
 *
 * Sibling-copy: "(sibling-copy)" is enough — operator knows they're
 * supplying the source themselves at build time.
 *
 * Sourced: include the URL + ref (defaulting to "main" if unset, since
 * source-detector normalizes to "main") and the in-container path.
 */
function describeSource(source: McpSource): string {
  if (source.install.kind === 'sibling-copy') {
    return `**${source.install.siblingDir}** (sibling-copy from build context) → \`${source.inContainerPath}\``;
  }

  const refSuffix = source.ref && source.ref !== 'main' ? `@${source.ref}` : '';
  return `**${source.url}**${refSuffix} → \`${source.inContainerPath}\``;
}

// ---------------------------------------------------------------------------
// Capability detection
// ---------------------------------------------------------------------------

/**
 * Heuristic: any mcpServer references `.zuliprc` (in args/command/env values)
 * OR has an env entry named `ZULIP_RC_PATH` OR has a source URL that
 * mentions "zulip". Catches the example shape (`env.ZULIP_RC_PATH`) and the
 * git-clone case (`source.url = ".../zulip_mcp.git"`).
 *
 * Same shape as compose.ts's anyRecipeUsesZulip but with the source-URL
 * check folded in so we light up the prereq line for builds that bake in a
 * Zulip MCP without the .zuliprc-mounting heuristic firing yet.
 */
function anyRecipeUsesZulip(walks: WalkResult[]): boolean {
  for (const walk of walks) {
    const servers = walk.recipe.mcpServers;
    if (!servers) continue;
    for (const server of Object.values(servers)) {
      if (mcpServerReferencesZulip(server)) return true;
    }
  }
  return false;
}

function mcpServerReferencesZulip(server: RecipeMcpServer): boolean {
  if (server.env) {
    if ('ZULIP_RC_PATH' in server.env) return true;
    for (const v of Object.values(server.env)) {
      if (typeof v === 'string' && v.includes('.zuliprc')) return true;
    }
  }
  if (server.command && server.command.includes('.zuliprc')) return true;
  if (server.args) {
    for (const arg of server.args) {
      if (arg.includes('.zuliprc')) return true;
    }
  }
  if (server.source?.url && server.source.url.toLowerCase().includes('zulip')) {
    return true;
  }
  return false;
}

/**
 * Heuristic for GitLab: any mcpServer with a key that matches "gitlab",
 * an args entry mentioning gitlab, an env entry named like a GitLab token,
 * or a source URL mentioning gitlab.
 */
function anyRecipeUsesGitLab(walks: WalkResult[]): boolean {
  for (const walk of walks) {
    const servers = walk.recipe.mcpServers;
    if (!servers) continue;
    for (const [name, server] of Object.entries(servers)) {
      if (name.toLowerCase().includes('gitlab')) return true;
      if (mcpServerReferencesGitLab(server)) return true;
    }
  }
  return false;
}

function mcpServerReferencesGitLab(server: RecipeMcpServer): boolean {
  if (server.env) {
    for (const k of Object.keys(server.env)) {
      if (k.toUpperCase().includes('GITLAB')) return true;
    }
  }
  if (server.args) {
    for (const arg of server.args) {
      if (arg.toLowerCase().includes('gitlab')) return true;
    }
  }
  if (server.source?.url && server.source.url.toLowerCase().includes('gitlab')) {
    return true;
  }
  return false;
}

// ---------------------------------------------------------------------------
// Workspace mount aggregation
// ---------------------------------------------------------------------------

interface PersistenceEntry {
  /** Host bind path, including leading `./`. */
  host: string;
  /** Container bind path. Mirrors compose.ts's `/app/<lastSegment>` shape. */
  container: string;
  /** Display string: `read-only` or `read-write`. */
  mode: string;
  /** Which recipe(s) declare this mount, for the right-most table column. */
  source: string;
}

/**
 * Collect every workspace mount from every walked recipe, deduplicated by
 * container path (matches compose.ts first-write-wins). The `source` column
 * lists every recipe that declared a mount with the same container path so
 * operators can trace a directory back to the agent that uses it.
 */
function collectWorkspaceMounts(walks: WalkResult[]): PersistenceEntry[] {
  const byContainer = new Map<string, PersistenceEntry>();

  for (const walk of walks) {
    const workspace = walk.recipe.modules?.workspace;
    if (!workspace || typeof workspace !== 'object' || !workspace.mounts) continue;

    const recipeLabel = walk.recipe.name;
    for (const mount of workspace.mounts) {
      const entry = mountToEntry(mount, recipeLabel);
      const existing = byContainer.get(entry.container);
      if (!existing) {
        byContainer.set(entry.container, entry);
        continue;
      }
      // Append recipe to the source column if it isn't already listed.
      const sources = existing.source.split(', ');
      if (!sources.includes(recipeLabel)) {
        existing.source = `${existing.source}, ${recipeLabel}`;
      }
    }
  }

  return Array.from(byContainer.values());
}

function mountToEntry(
  mount: RecipeWorkspaceMount,
  recipeLabel: string,
): PersistenceEntry {
  const stripped = mount.path.replace(/^\.\//, '').replace(/^\/+/, '');
  const lastSegment = stripped.split('/').filter(Boolean).pop() ?? slugify(mount.name);
  return {
    host: `./${stripped}`,
    container: `/app/${lastSegment}`,
    mode: mount.mode === 'read-only' ? 'read-only' : 'read-write',
    source: recipeLabel,
  };
}

