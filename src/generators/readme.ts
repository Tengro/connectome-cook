/**
 * README generator — Phase 2 of BUILD-PLAN.md.
 *
 * Templates an operator-facing README that lives next to the generated
 * Dockerfile + docker-compose.yml. The audience is "someone who just got
 * the cook output dir and now needs to make it run on their box": tells
 * them what the image bundles, what they need to bring (env vars, .zuliprc),
 * how to build/attach/detach, where persistent state lives, and the small
 * set of trip-wires they'll otherwise hit.
 *
 * Reference for visual review (NOT a diff target):
 *   examples/triumvirate/README.md — hand-tuned for the Triumvirate. Cook's
 *   README must be data-driven enough to make sense for any recipe; the
 *   shape and section ordering match the example, the prose is generic.
 *
 * Pure string templating with template literals — no markdown library, no
 * helper engine. Operators paste this into editors (VS Code, neovim) and
 * read it via GitHub renderers; we stick to plain CommonMark.
 *
 * Sections (in order):
 *   1. Title + 1-paragraph description (from walks[0].recipe.description).
 *   2. What's in the image — the cloned ref of connectome-host, language
 *      runtimes, MCP sources, baked-in recipes.
 *   3. Prerequisites — Docker, Anthropic key, plus per-source items we can
 *      introspect (Zulip, GitLab) — fall back to generic "set vars in .env".
 *   4. Quick start — 4 numbered shell steps (cp .env.example, fill prereq
 *      files, build, attach).
 *   5. Detach without stopping — Ctrl+P/Ctrl+Q dance + the /quit trap.
 *   6. Persistence — table of every workspace mount → host bind path.
 *   7. Troubleshooting — small canned set of "you'll hit this" items.
 *   8. What this image is NOT — short caveats.
 *   9. Footer — generator version + how to regenerate.
 */

import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import type {
  GeneratorInput,
  McpSource,
  WalkResult,
} from '../types.js';
import type {
  Recipe,
  RecipeMcpServer,
  RecipeWorkspaceMount,
} from '../vendor/recipe.js';
import { slugify } from '../slug.js';

